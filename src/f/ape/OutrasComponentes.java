package f.ape;



/**
 * Classe que possui Outras Componentes de avaliação tais como comportamente,
 * assiduidade e etc. todos emglobados na mesma classe.
 * @author Utilizador
 * @version 1.0
 * @created 14-Mai-2012 17:08:34
 */
public class OutrasComponentes {

	/**
	 * Nota das Outras Componentes
	 */
	private double nota;
	/**
	 * Percentagem das Outras Componentes para a nota final.
	 */
	private double percentagem;

	public OutrasComponentes(){

	}

	public void finalize() throws Throwable {

	}

	public double getNota() {
		return nota;
	}

	public void setNota(double nota) {
		this.nota = nota;
	}

	public double getPercentagem() {
		return percentagem;
	}

	public void setPercentagem(double percentagem) {
		this.percentagem = percentagem;
	}

}